#include "funciones.h"

/* Implementacion de funciones */

int functionSuma(int nro1, int nro2) {
    return nro1 + nro2; //el retorno no muestra salvo con printf
}

int functionResta(int nro1, int nro2) {
    return nro1 - nro2;
}

int functionMultiplicacion(int nro1, int nro2) {
    return nro1 * nro2;
}

int functionDivision(int nro1, int nro2) {
    return (float) nro1 / (float) nro2;
}
